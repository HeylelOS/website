		<h2>What is HeylelOS?</h2>
		<p>
		HeylelOS is an open source project aimed at creating a modern unix-like operating system.
		It aims to be reliable, simple, modular and elegant.
		</p>
		
		<h2>Why?</h2>
		<p>
		When I discovered the wonderful world of open source Operating Systems, from Linux to BSDs, I was deceived to discover heavy systems.
		Those stacked years of contributions into, maybe reliable, but old softwares, based upon old philosophies and technologies.
		It isn't wrong in many cases, but sometimes brought incompatibilities and a wrong focus on the user experience.
		</p>
		<p>
		Some of those open source systems I discovered satisfied me.
		But I wanted something which would have mirrored my philosophy.
		So I decided to work and learn more and more about UNIX to bring my system to life.
		</p>
		<p>
		Therefore, I planned on creating a system beginners would love, and insiders would be curious to discover.
		</p>

		<h2>How?</h2>
		<p>
		By selecting, creating and maintaining a stable ecosystem of applications on each machine.
		And using modern approaches, and errors and solutions from the past, to bring the best user-experience for an OS.
		</p>

		<h2>Why the name "Heylel"?</h2>
		<p>
		"Heylel" is a rough transliteration of the original hebrew word translated in the Latin Bible to "Lucifer" (Book of Isaiah 14.12.).
		It designated the "morning star, Venus" but could also mean "light-bringing".
		It's an echo to the UNIX "mythology" around daemons, but also a reference to Prometheus, the "light-bringing" Titan who brought knowledge to mankind.
		</p>
